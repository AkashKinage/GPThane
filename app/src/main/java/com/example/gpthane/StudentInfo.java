package com.example.gpthane;

public class StudentInfo {

    public String name, enrollmentno, phone, email;

    public StudentInfo(){

    }

    public StudentInfo(String name, String enrollmentno, String phone, String email) {
        this.name = name;
        this.enrollmentno = enrollmentno;
        this.phone = phone;
        this.email = email;
    }
}
