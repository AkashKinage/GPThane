package com.example.gpthane.Student;

public class StudentInfo {

    public String name, enrollmentno, phone, email, department, year, role;

    public StudentInfo(){

    }

    public StudentInfo(String name, String enrollmentno, String phone, String email, String department, String year, String role) {
        this.name = name;
        this.enrollmentno = enrollmentno;
        this.phone = phone;
        this.email = email;
        this.department = department;
        this.year = year;
        this.role = role;
    }
}
