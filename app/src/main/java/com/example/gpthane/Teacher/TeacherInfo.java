package com.example.gpthane.Teacher;

public class TeacherInfo {

    public String name, email, phone, department, role;

    public TeacherInfo(){

    }

    public TeacherInfo(String name, String email, String phone, String department, String role) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.department = department;
        this.role = role;
    }
}
